package com.shenmintech.cbp.biz.vo;

public enum SwitchStatus {

  TURN_OFF("关闭", "1"), TURN_ON("打开", "0");
  
  private String desc;
  private String code;

  private SwitchStatus(String desc, String code) {
    this.desc = desc;
    this.code = code;
  }

  public String getDesc() {
    return desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }

  public String getCode() {
    return code;
  }

  public void setCode(String code) {
    this.code = code;
  }
  
}
