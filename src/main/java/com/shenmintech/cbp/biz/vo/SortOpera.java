package com.shenmintech.cbp.biz.vo;

public enum SortOpera {

  STICK_TOP("置顶", "0"), MOVE_UP("上移", "1"), MOVE_DOWN("下移","2");
  
  private String desc;
  private String code;

  public String getDesc() {
    return desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }

  public String getCode() {
    return code;
  }

  public void setCode(String code) {
    this.code = code;
  }

  private SortOpera(String desc, String code) {
    this.desc = desc;
    this.code = code;
  }
}
