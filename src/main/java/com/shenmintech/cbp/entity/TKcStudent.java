package com.shenmintech.cbp.entity;

import java.util.Date;

public class TKcStudent {
  private String id;

  private String employeeFid;

  private String groupFid;

  private Date createTime;

  private Date updateTime;

  private Integer status;

  private String remark1;

  private String remark2;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getEmployeeFid() {
    return employeeFid;
  }

  public void setEmployeeFid(String employeeFid) {
    this.employeeFid = employeeFid;
  }

  public String getGroupFid() {
    return groupFid;
  }

  public void setGroupFid(String groupFid) {
    this.groupFid = groupFid;
  }

  public Date getCreateTime() {
    return createTime;
  }

  public void setCreateTime(Date createTime) {
    this.createTime = createTime;
  }

  public Date getUpdateTime() {
    return updateTime;
  }

  public void setUpdateTime(Date updateTime) {
    this.updateTime = updateTime;
  }

  public Integer getStatus() {
    return status;
  }

  public void setStatus(Integer status) {
    this.status = status;
  }

  public String getRemark1() {
    return remark1;
  }

  public void setRemark1(String remark1) {
    this.remark1 = remark1;
  }

  public String getRemark2() {
    return remark2;
  }

  public void setRemark2(String remark2) {
    this.remark2 = remark2;
  }
}
